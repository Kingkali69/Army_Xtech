class PluginResourceMonitor:
    """Monitors and enforces resource limits for plugins"""
    
    def __init__(self, plugin_manager: PluginManager):
        self.plugin_manager = plugin_manager
        self.resource_usage: Dict[str, Dict[str, Any]] = {}
        self.monitoring_active = False
        self.monitor_thread = None
        
    def start_monitoring(self):
        """Start resource monitoring thread"""
        if not self.monitoring_active:
            self.monitoring_active = True
            self.monitor_thread = threading.Thread(target=self._monitor_resources, daemon=True)
            self.monitor_thread.start()
    
    def stop_monitoring(self):
        """Stop resource monitoring"""
        self.monitoring_active = False
        if self.monitor_thread:
            self.monitor_thread.join(timeout=5)
    
    def _monitor_resources(self):
        """Main monitoring loop"""
        import psutil
        
        while self.monitoring_active:
            try:
                for plugin_name, plugin in self.plugin_manager.loaded_plugins.items():
                    manifest = plugin.manifest
                    limits = manifest.resource_limits
                    
                    if not limits:
                        continue
                    
                    # Get current resource usage
                    current_usage = self._get_plugin_resource_usage(plugin_name)
                    self.resource_usage[plugin_name] = current_usage
                    
                    # Check limits
                    violations = []
                    
                    if 'max_memory_mb' in limits:
                        memory_mb = current_usage['memory_bytes'] / (1024 * 1024)
                        if memory_mb > limits['max_memory_mb']:
                            violations.append(f"Memory: {memory_mb:.1f}MB > {limits['max_memory_mb']}MB")
                    
                    if 'max_cpu_percent' in limits:
                        cpu_percent = current_usage['cpu_percent']
                        if cpu_percent > limits['max_cpu_percent']:
                            violations.append(f"CPU: {cpu_percent:.1f}% > {limits['max_cpu_percent']}%")
                    
                    if violations:
                        self._handle_resource_violation(plugin_name, violations)
                
                time.sleep(10)  # Check every 10 seconds
                
            except Exception as e:
                print(f"Resource monitoring error: {e}")
                time.sleep(5)
    
    def _get_plugin_resource_usage(self, plugin_name: str) -> Dict[str, Any]:
        """Get resource usage for a specific plugin"""
        try:
            import psutil
            process = psutil.Process()
            
            return {
                'memory_bytes': process.memory_info().rss,
                'cpu_percent': process.cpu_percent(),
                'threads': process.num_threads(),
                'open_files': len(process.open_files()),
                'timestamp': datetime.now()
            }
        except Exception:
            return {
                'memory_bytes': 0,
                'cpu_percent': 0,
                'threads': 0,
                'open_files': 0,
                'timestamp': datetime.now()
            }
    
    def _handle_resource_violation(self, plugin_name: str, violations: List[str]):
        """Handle resource limit violations"""
        print(f"⚠️ Resource violations for plugin '{plugin_name}': {', '.join(violations)}")
        
        # Suspend plugin if violations are severe
        if plugin_name in self.plugin_manager.loaded_plugins:
            plugin = self.plugin_manager.loaded_plugins[plugin_name]
            plugin._set_state(PluginState.SUSPENDED)
            
            # Log violation
            if plugin_name in self.plugin_manager.plugin_metrics:
                metrics = self.plugin_manager.plugin_metrics[plugin_name]
                metrics.setdefault('resource_violations', []).append({
                    'timestamp': datetime.now(),
                    'violations': violations
                })

class PluginHotReloader:
    """Handles hot reloading of plugins during development"""
    
    def __init__(self, plugin_manager: PluginManager):
        self.plugin_manager = plugin_manager
        self.file_watchers: Dict[str, Any] = {}
        self.watching = False
        
    def start_watching(self):
        """Start watching plugin files for changes"""
        try:
            import watchdog.observers
            import watchdog.events
            
            self.watching = True
            
            class PluginFileHandler(watchdog.events.FileSystemEventHandler):
                def __init__(self, hot_reloader):
                    self.hot_reloader = hot_reloader
                
                def on_modified(self, event):
                    if not event.is_directory and event.src_path.endswith(('.py', '.kkg')):
                        self.hot_reloader._handle_file_change(Path(event.src_path))
            
            observer = watchdog.observers.Observer()
            handler = PluginFileHandler(self)
            
            for directory in self.plugin_manager.plugin_directories:
                if directory.exists():
                    observer.schedule(handler, str(directory), recursive=False)
            
            observer.start()
            self.observer = observer
            
        except ImportError:
            print("⚠️ Watchdog not available - hot reloading disabled")
    
    def stop_watching(self):
        """Stop watching files"""
        self.watching = False
        if hasattr(self, 'observer'):
            self.observer.stop()
            self.observer.join()
    
    def _handle_file_change(self, file_path: Path):
        """Handle plugin file changes"""
        if not self.watching:
            return
        
        # Find which plugin this file belongs to
        for plugin_name, descriptor in self.plugin_manager.discovery.discovered_plugins.items():
            if descriptor.path == file_path:
                print(f"🔄 Plugin file changed: {plugin_name}")
                
                # Reload the plugin
                success = self.plugin_manager.reload_plugin(plugin_name)
                if success:
                    print(f"✅ Hot reloaded plugin: {plugin_name}")
                else:
                    print(f"❌ Failed to hot reload plugin: {plugin_name}")
                break

