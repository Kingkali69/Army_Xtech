class PluginEncryption:
    """Handles encryption/decryption of plugin modules for IP protection"""
    
    def __init__(self, master_key: bytes):
        self.fernet = Fernet(master_key)
    
    @classmethod
    def generate_key(cls, password: str, salt: bytes = None) -> bytes:
        """Generate encryption key from password"""
        if salt is None:
            salt = os.urandom(16)
        
        kdf = PBKDF2HMAC(
            algorithm=hashes.SHA256(),
            length=32,
            salt=salt,
            iterations=100000,
        )
        key = base64.urlsafe_b64encode(kdf.derive(password.encode()))
        return key
    
    def encrypt_plugin(self, source_code: str) -> bytes:
        """Encrypt plugin source code"""
        return self.fernet.encrypt(source_code.encode())
    
    def decrypt_plugin(self, encrypted_data: bytes) -> str:
        """Decrypt plugin source code"""
        return self.fernet.decrypt(encrypted_data).decode()

@dataclass
class PluginDescriptor:
    """Internal descriptor for discovered plugins"""
    name: str
    path: Path
    manifest: PluginManifest
    encrypted: bool = False
    checksum: str = ""
    last_modified: datetime = field(default_factory=datetime.now)
    
class PluginDiscovery:
    """Discovers and catalogs available plugins"""
    
    def __init__(self, plugin_directories: List[Path], encryption: Optional[PluginEncryption] = None):
        self.plugin_directories = plugin_directories
        self.encryption = encryption
        self.discovered_plugins: Dict[str, PluginDescriptor] = {}
    
    def discover_plugins(self) -> Dict[str, PluginDescriptor]:
        """Discover all available plugins"""
        self.discovered_plugins.clear()
        
        for directory in self.plugin_directories:
            if not directory.exists():
                continue
            
            # Look for .py files and .kkg (encrypted) files
            for pattern in ["*.py", "*.kkg"]:
                for plugin_file in directory.glob(pattern):
                    try:
                        descriptor = self._analyze_plugin_file(plugin_file)
                        if descriptor:
                            self.discovered_plugins[descriptor.name] = descriptor
                    except Exception as e:
                        print(f"Error analyzing plugin {plugin_file}: {e}")
        
        return self.discovered_plugins
    
    def _analyze_plugin_file(self, plugin_path: Path) -> Optional[PluginDescriptor]:
        """Analyze a single plugin file"""
        try:
            encrypted = plugin_path.suffix == '.kkg'
            
            if encrypted:
                if not self.encryption:
                    return None
                
                # Decrypt and analyze
                with open(plugin_path, 'rb') as f:
                    encrypted_data = f.read()
                source_code = self.encryption.decrypt_plugin(encrypted_data)
            else:
                with open(plugin_path, 'r') as f:
                    source_code = f.read()
            
            # Extract manifest from source code
            manifest = self._extract_manifest(source_code, plugin_path.stem)
            
            if manifest:
                checksum = hashlib.sha256(source_code.encode()).hexdigest()
                last_modified = datetime.fromtimestamp(plugin_path.stat().st_mtime)
                
                return PluginDescriptor(
                    name=manifest.name,
                    path=plugin_path,
                    manifest=manifest,
                    encrypted=encrypted,
                    checksum=checksum,
                    last_modified=last_modified
                )
        
        except Exception as e:
            print(f"Failed to analyze plugin {plugin_path}: {e}")
            return None
    
    def _extract_manifest(self, source_code: str, default_name: str) -> Optional[PluginManifest]:
        """Extract plugin manifest from source code"""
        try:
            # Look for PLUGIN_MANIFEST constant
            namespace = {}
            exec(source_code, namespace)
            
            if 'PLUGIN_MANIFEST' in namespace:
                manifest_data = namespace['PLUGIN_MANIFEST']
                
                # Convert to PluginManifest object
                return PluginManifest(
                    name=manifest_data.get('name', default_name),
                    version=manifest_data.get('version', '1.0.0'),
                    author=manifest_data.get('author', 'Unknown'),
                    description=manifest_data.get('description', ''),
                    capabilities=[PluginCapability(c) for c in manifest_data.get('capabilities', [])],
                    dependencies=manifest_data.get('dependencies', []),
                    platform_requirements=manifest_data.get('platform_requirements', []),
                    event_types=[SecurityEventType(t) for t in manifest_data.get('event_types', [])],
                    api_version=manifest_data.get('api_version', '1.0'),
                    license_required=manifest_data.get('license_required', True),
                    config_schema=manifest_data.get('config_schema', {}),
                    resource_limits=manifest_data.get('resource_limits', {})
                )
        
        except Exception as e:
            print(f"Failed to extract manifest: {e}")
        
        return None

