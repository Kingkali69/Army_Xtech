class PluginValidator:
    """Validates plugin code and configurations"""
    
    def __init__(self):
        self.validation_rules = [
            self._check_manifest_present,
            self._check_required_methods,
            self._check_imports,
            self._check_security_patterns,
            self._check_resource_usage,
        ]
    
    def validate_plugin(self, plugin_path: Path) -> Dict[str, Any]:
        """Validate a plugin file"""
        validation_results = {
            'valid': True,
            'errors': [],
            'warnings': [],
            'suggestions': []
        }
        
        try:
            with open(plugin_path, 'r') as f:
                source_code = f.read()
            
            for rule in self.validation_rules:
                rule_result = rule(source_code, plugin_path)
                
                validation_results['errors'].extend(rule_result.get('errors', []))
                validation_results['warnings'].extend(rule_result.get('warnings', []))
                validation_results['suggestions'].extend(rule_result.get('suggestions', []))
                
                if rule_result.get('errors'):
                    validation_results['valid'] = False
            
        except Exception as e:
            validation_results['valid'] = False
            validation_results['errors'].append(f"Failed to read plugin file: {e}")
        
        return validation_results
    
    def _check_manifest_present(self, source_code: str, plugin_path: Path) -> Dict[str, List[str]]:
        """Check if plugin manifest is present"""
        result = {'errors': [], 'warnings': [], 'suggestions': []}
        
        if 'PLUGIN_MANIFEST' not in source_code:
            result['errors'].append("Missing PLUGIN_MANIFEST constant")
        
        return result
    
    def _check_required_methods(self, source_code: str, plugin_path: Path) -> Dict[str, List[str]]:
        """Check if required methods are implemented"""
        result = {'errors': [], 'warnings': [], 'suggestions': []}
        
        required_methods = ['initialize', 'manifest']
        
        for method in required_methods:
            if f'def {method}(' not in source_code:
                result['errors'].append(f"Missing required method: {method}")
        
        return result
    
    def _check_imports(self, source_code: str, plugin_path: Path) -> Dict[str, List[str]]:
        """Check for dangerous imports"""
        result = {'errors': [], 'warnings': [], 'suggestions': []}
        
        dangerous_imports = ['os', 'sys', 'subprocess', 'eval', 'exec']
        
        for imp in dangerous_imports:
            if f'import {imp}' in source_code or f'from {imp}' in source_code:
                result['warnings'].append(f"Potentially dangerous import: {imp}")
        
        return result
    
    def _check_security_patterns(self, source_code: str, plugin_path: Path) -> Dict[str, List[str]]:
        """Check for security anti-patterns"""
        result = {'errors': [], 'warnings': [], 'suggestions': []}
        
        security_patterns = [
            ('eval(', 'Use of eval() function'),
            ('exec(', 'Use of exec() function'),
            ('__import__(', 'Direct use of __import__'),
            ('shell=True', 'Shell command execution'),
        ]
        
        for pattern, message in security_patterns:
            if pattern in source_code:
                result['warnings'].append(f"Security concern: {message}")
        
        return result
    
    def _check_resource_usage(self, source_code: str, plugin_path: Path) -> Dict[str, List[str]]:
        """Check for resource usage patterns"""
        result = {'errors': [], 'warnings': [], 'suggestions': []}
        
        if 'while True:' in source_code and 'time.sleep(' not in source_code:
            result['warnings'].append("Infinite loop without sleep - may consume excessive CPU")
        
        if 'threading.Thread(' in source_code:
            if 'daemon=True' not in source_code:
                result['suggestions'].append("Consider using daemon threads for background tasks")
        
        return result

