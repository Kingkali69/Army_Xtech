class SecurityPlugin(abc.ABC):
    """
    Base class for all security plugins in the KK&G framework.
    Provides lifecycle management, event processing, and resource management.
    """
    
    def __init__(self):
        self._state = PluginState.UNKNOWN
        self._config: Dict[str, Any] = {}
        self._metrics: Dict[str, Any] = {}
        self._logger = None
        self._event_handlers: Dict[SecurityEventType, Callable] = {}
        self._lock = threading.RLock()
        
    @property
    @abc.abstractmethod
    def manifest(self) -> PluginManifest:
        """Plugin manifest defining capabilities and requirements"""
        pass
    
    @abc.abstractmethod
    def initialize(self, config: Dict[str, Any], logger) -> bool:
        """
        Initialize the plugin with configuration.
        Called once when plugin is loaded.
        
        Args:
            config: Plugin-specific configuration dictionary
            logger: Framework logger instance
            
        Returns:
            True if initialization successful, False otherwise
        """
        pass
    
    @abc.abstractmethod
    def process_event(self, event: SecurityEvent) -> List[ResponseAction]:
        """
        Process a security event and return response actions.
        
        Args:
            event: Security event to process
            
        Returns:
            List of response actions to execute
        """
        pass
    
    def get_health_status(self) -> Dict[str, Any]:
        """
        Return current plugin health and metrics.
        Override for custom health checks.
        """
        return {
            "state": self._state.name,
            "last_activity": datetime.now().isoformat(),
            "metrics": self._metrics.copy(),
            "config_valid": bool(self._config),
            "memory_usage": self._get_memory_usage()
        }
    
    def get_configuration_schema(self) -> Dict[str, Any]:
        """Return JSON schema for plugin configuration"""
        return self.manifest.config_schema
    
    def validate_configuration(self, config: Dict[str, Any]) -> bool:
        """Validate configuration against schema"""
        # Basic validation - can be extended with jsonschema
        required_fields = self.manifest.config_schema.get("required", [])
        return all(field in config for field in required_fields)
    
    def shutdown(self):
        """Cleanup when plugin is being unloaded"""
        with self._lock:
            self._state = PluginState.UNLOADING
            # Override in subclasses for custom cleanup
    
    def _get_memory_usage(self) -> int:
        """Get approximate memory usage in bytes"""
        try:
            import psutil
            import os
            process = psutil.Process(os.getpid())
            return process.memory_info().rss
        except ImportError:
            return 0
    
    # Internal framework methods - do not override
    def _set_state(self, state: PluginState):
        """Internal: Set plugin state"""
        with self._lock:
            self._state = state
    
    def _get_state(self) -> PluginState:
        """Internal: Get current plugin state"""
        return self._state
    
    def _set_config(self, config: Dict[str, Any]):
        """Internal: Set plugin configuration"""
        self._config = config.copy()

class EventProcessorPlugin(SecurityPlugin):
    """Specialized plugin for event processing"""
    
    def register_event_handler(self, event_type: SecurityEventType, 
                             handler: Callable[[SecurityEvent], List[ResponseAction]]):
        """Register a handler for specific event types"""
        self._event_handlers[event_type] = handler
    
    def process_event(self, event: SecurityEvent) -> List[ResponseAction]:
        """Route event to appropriate handler"""
        handler = self._event_handlers.get(event.event_type)
        if handler:
            return handler(event)
        return []

class ThreatDetectionPlugin(SecurityPlugin):
    """Specialized plugin for threat detection"""
    
    @abc.abstractmethod
    def detect_threats(self, event: SecurityEvent) -> List[Dict[str, Any]]:
        """
        Detect threats in the given event.
        
        Returns:
            List of threat indicators with confidence scores
        """
        pass
    
    def process_event(self, event: SecurityEvent) -> List[ResponseAction]:
        """Default implementation that converts threats to alerts"""
        threats = self.detect_threats(event)
        actions = []
        
        for threat in threats:
            action = ResponseAction(
                action_id=f"threat_alert_{threat.get('id', 'unknown')}",
                action_type=ResponseActionType.ALERT,
                priority=threat.get('confidence', 5),
                target=event.source,
                parameters={
                    "threat_type": threat.get('type'),
                    "confidence": threat.get('confidence'),
                    "details": threat.get('details', {})
                }
            )
            actions.append(action)
        
        return actions

