@dataclass
class SecurityEvent:
    """Immutable security event passed to plugins"""
    event_id: str
    timestamp: datetime
    event_type: SecurityEventType
    source: str
    data: Dict[str, Any]
    severity: int  # 0-10 scale
    tags: Set[str] = field(default_factory=set)
    metadata: Dict[str, Any] = field(default_factory=dict)
    
    def __post_init__(self):
        """Ensure immutability"""
        self.data = dict(self.data)  # Create copy
        self.tags = set(self.tags)   # Create copy
        self.metadata = dict(self.metadata)

