class PluginManager:
    """
    Core plugin manager handling loading, lifecycle, dependency resolution,
    and runtime management of security plugins.
    """
    
    def __init__(self, 
                 plugin_directories: List[Path],
                 encryption_key: Optional[bytes] = None,
                 max_concurrent_plugins: int = 50):
        
        self.plugin_directories = plugin_directories
        self.encryption = PluginEncryption(encryption_key) if encryption_key else None
        self.max_concurrent_plugins = max_concurrent_plugins
        
        # Plugin management
        self.discovery = PluginDiscovery(plugin_directories, self.encryption)
        self.loaded_plugins: Dict[str, SecurityPlugin] = {}
        self.plugin_modules: Dict[str, Any] = {}  # Loaded Python modules
        self.plugin_configs: Dict[str, Dict[str, Any]] = {}
        self.plugin_dependencies: Dict[str, Set[str]] = {}
        
        # Runtime management
        self.event_queue = queue.Queue()
        self.response_queue = queue.Queue()
        self.executor = ThreadPoolExecutor(max_workers=max_concurrent_plugins)
        self.plugin_locks: Dict[str, threading.RLock] = {}
        
        # Metrics and monitoring
        self.plugin_metrics: Dict[str, Dict[str, Any]] = {}
        self.load_order: List[str] = []
        
        # Lifecycle callbacks
        self.lifecycle_callbacks: Dict[str, List[Callable]] = {
            'on_plugin_loaded': [],
            'on_plugin_unloaded': [],
            'on_plugin_error': []
        }
    
    def discover_plugins(self) -> Dict[str, PluginDescriptor]:
        """Discover all available plugins"""
        return self.discovery.discover_plugins()
    
    def load_plugin(self, plugin_name: str, config: Dict[str, Any] = None) -> bool:
        """
        Load and initialize a specific plugin.
        
        Args:
            plugin_name: Name of plugin to load
            config: Plugin-specific configuration
            
        Returns:
            True if loaded successfully, False otherwise
        """
        try:
            # Check if already loaded
            if plugin_name in self.loaded_plugins:
                return True
            
            # Discover if not already done
            if plugin_name not in self.discovery.discovered_plugins:
                self.discover_plugins()
            
            descriptor = self.discovery.discovered_plugins.get(plugin_name)
            if not descriptor:
                raise ValueError(f"Plugin '{plugin_name}' not found")
            
            # Validate dependencies
            if not self._validate_dependencies(descriptor):
                raise ValueError(f"Plugin '{plugin_name}' has unmet dependencies")
            
            # Load the plugin module
            plugin_module = self._load_plugin_module(descriptor)
            if not plugin_module:
                raise ValueError(f"Failed to load plugin module for '{plugin_name}'")
            
            # Instantiate plugin class
            plugin_instance = self._instantiate_plugin(plugin_module, descriptor)
            if not plugin_instance:
                raise ValueError(f"Failed to instantiate plugin '{plugin_name}'")
            
            # Initialize plugin
            final_config = config or self.plugin_configs.get(plugin_name, {})
            if not plugin_instance.validate_configuration(final_config):
                raise ValueError(f"Invalid configuration for plugin '{plugin_name}'")
            
            plugin_instance._set_config(final_config)
            
            if not plugin_instance.initialize(final_config, self._create_plugin_logger(plugin_name)):
                raise ValueError(f"Plugin '{plugin_name}' initialization failed")
            
            # Register plugin
            self.loaded_plugins[plugin_name] = plugin_instance
            self.plugin_modules[plugin_name] = plugin_module
            self.plugin_locks[plugin_name] = threading.RLock()
            self.load_order.append(plugin_name)
            
            plugin_instance._set_state(PluginState.ACTIVE)
            
            # Initialize metrics
            self.plugin_metrics[plugin_name] = {
                'load_time': datetime.now(),
                'events_processed': 0,
                'responses_generated': 0,
                'errors': 0,
                'last_activity': datetime.now()
            }
            
            # Notify callbacks
            for callback in self.lifecycle_callbacks['on_plugin_loaded']:
                try:
                    callback(plugin_name, plugin_instance)
                except Exception as e:
                    print(f"Lifecycle callback error: {e}")
            
            print(f"✅ Plugin '{plugin_name}' loaded successfully")
            return True
            
        except Exception as e:
            print(f"❌ Failed to load plugin '{plugin_name}': {e}")
            self._handle_plugin_error(plugin_name, e)
            return False
    
    def unload_plugin(self, plugin_name: str) -> bool:
        """Unload a specific plugin"""
        try:
            if plugin_name not in self.loaded_plugins:
                return True
            
            plugin = self.loaded_plugins[plugin_name]
            plugin._set_state(PluginState.UNLOADING)
            
            # Call shutdown
            plugin.shutdown()
            
            # Remove from active plugins
            del self.loaded_plugins[plugin_name]
            if plugin_name in self.plugin_modules:
                del self.plugin_modules[plugin_name]
            if plugin_name in self.plugin_locks:
                del self.plugin_locks[plugin_name]
            if plugin_name in self.load_order:
                self.load_order.remove(plugin_name)
            
            # Notify callbacks
            for callback in self.lifecycle_callbacks['on_plugin_unloaded']:
                try:
                    callback(plugin_name, plugin)
                except Exception as e:
                    print(f"Lifecycle callback error: {e}")
            
            print(f"✅ Plugin '{plugin_name}' unloaded successfully")
            return True
            
        except Exception as e:
            print(f"❌ Failed to unload plugin '{plugin_name}': {e}")
            return False
    
    def reload_plugin(self, plugin_name: str) -> bool:
        """Reload a plugin (unload then load)"""
        config = self.plugin_configs.get(plugin_name, {})
        
        if plugin_name in self.loaded_plugins:
            if not self.unload_plugin(plugin_name):
                return False
        
        return self.load_plugin(plugin_name, config)
    
    def process_event(self, event: SecurityEvent) -> List[ResponseAction]:
        """Process event through all relevant plugins"""
        responses = []
        
        for plugin_name, plugin in self.loaded_plugins.items():
            try:
                if plugin._get_state() != PluginState.ACTIVE:
                    continue
                
                # Check if plugin handles this event type
                if (event.event_type in plugin.manifest.event_types or 
                    not plugin.manifest.event_types):
                    
                    with self.plugin_locks[plugin_name]:
                        plugin_responses = plugin.process_event(event)
                        responses.extend(plugin_responses or [])
                        
                        # Update metrics
                        self.plugin_metrics[plugin_name]['events_processed'] += 1
                        self.plugin_metrics[plugin_name]['responses_generated'] += len(plugin_responses or [])
                        self.plugin_metrics[plugin_name]['last_activity'] = datetime.now()
                        
            except Exception as e:
                self._handle_plugin_error(plugin_name, e)
                self.plugin_metrics[plugin_name]['errors'] += 1
        
        return responses
    
    def get_plugin_status(self, plugin_name: str = None) -> Dict[str, Any]:
        """Get status of specific plugin or all plugins"""
        if plugin_name:
            if plugin_name in self.loaded_plugins:
                plugin = self.loaded_plugins[plugin_name]
                return {
                    'name': plugin_name,
                    'state': plugin._get_state().name,
                    'health': plugin.get_health_status(),
                    'metrics': self.plugin_metrics.get(plugin_name, {}),
                    'manifest': plugin.manifest.__dict__
                }
            else:
                return {'error': f'Plugin {plugin_name} not loaded'}
        
        # Return status for all plugins
        status = {}
        for name in self.loaded_plugins:
            status[name] = self.get_plugin_status(name)
        
        return status
    
    def set_plugin_config(self, plugin_name: str, config: Dict[str, Any]):
        """Set configuration for a plugin"""
        self.plugin_configs[plugin_name] = config
        
        # If plugin is loaded, reconfigure it
        if plugin_name in self.loaded_plugins:
            plugin = self.loaded_plugins[plugin_name]
            if plugin.validate_configuration(config):
                plugin._set_config(config)
                # Optionally trigger reconfiguration
    
    def add_lifecycle_callback(self, event: str, callback: Callable):
        """Add lifecycle event callback"""
        if event in self.lifecycle_callbacks:
            self.lifecycle_callbacks[event].append(callback)
    
    def get_loaded_plugins(self) -> List[str]:
        """Get list of loaded plugin names"""
        return list(self.loaded_plugins.keys())
    
    def get_available_plugins(self) -> List[str]:
        """Get list of available plugin names"""
        return list(self.discovery.discovered_plugins.keys())
    
    # Private methods
    
    def _validate_dependencies(self, descriptor: PluginDescriptor) -> bool:
        """Validate that plugin dependencies are met"""
        for dep in descriptor.manifest.dependencies:
            if dep not in self.loaded_plugins:
                # Try to load dependency
                if not self.load_plugin(dep):
                    return False
        return True
    
    def _load_plugin_module(self, descriptor: PluginDescriptor) -> Optional[Any]:
        """Load Python module for plugin"""
        try:
            if descriptor.encrypted and self.encryption:
                # Load encrypted plugin
                with open(descriptor.path, 'rb') as f:
                    encrypted_data = f.read()
                source_code = self.encryption.decrypt_plugin(encrypted_data)
                
                # Create module from source
                spec = importlib.util.spec_from_loader(descriptor.name, loader=None)
                module = importlib.util.module_from_spec(spec)
                exec(source_code, module.__dict__)
                
                return module
            else:
                # Load regular Python file
                spec = importlib.util.spec_from_file_location(descriptor.name, descriptor.path)
                module = importlib.util.module_from_spec(spec)
                spec.loader.exec_module(module)
                
                return module
                
        except Exception as e:
            print(f"Failed to load module for {descriptor.name}: {e}")
            return None
    
    def _instantiate_plugin(self, module: Any, descriptor: PluginDescriptor) -> Optional[SecurityPlugin]:
        """Find and instantiate plugin class from module"""
        try:
            # Look for classes that inherit from SecurityPlugin
            for name, obj in inspect.getmembers(module):
                if (inspect.isclass(obj) and 
                    issubclass(obj, SecurityPlugin) and 
                    obj != SecurityPlugin):
                    
                    return obj()
            
            return None
            
        except Exception as e:
            print(f"Failed to instantiate plugin from {descriptor.name}: {e}")
            return None
    
    def _create_plugin_logger(self, plugin_name: str):
        """Create logger instance for plugin"""
        # Return framework logger or create plugin-specific logger
        return None  # Placeholder - implement logging framework integration
    
    def _handle_plugin_error(self, plugin_name: str, error: Exception):
        """Handle plugin errors"""
        print(f"Plugin error in {plugin_name}: {error}")
        traceback.print_exc()
        
        # Notify error callbacks
        for callback in self.lifecycle_callbacks['on_plugin_error']:
            try:
                callback(plugin_name, error)
            except Exception as callback_error:
                print(f"Error callback failed: {callback_error}")
        
        # Consider suspending plugin if too many errors
        if plugin_name in self.plugin_metrics:
            error_count = self.plugin_metrics[plugin_name].get('errors', 0)
            if error_count > 10:  # Configurable threshold
                if plugin_name in self.loaded_plugins:
                    self.loaded_plugins[plugin_name]._set_state(PluginState.ERROR)

