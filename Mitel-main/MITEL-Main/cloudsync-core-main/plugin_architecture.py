class NetworkAnomalyPlugin(ThreatDetectionPlugin):
    """Example plugin for network anomaly detection"""
    
    @property
    def manifest(self) -> PluginManifest:
        return PluginManifest(
            name="network_anomaly_detector",
            version="1.0.0",
            author="KK&G DevOps",
            description="Detects network anomalies using statistical analysis",
            capabilities=[PluginCapability.THREAT_DETECTION, PluginCapability.EVENT_PROCESSING],
            event_types=[SecurityEventType.NETWORK_ANOMALY],
            config_schema={
                "type": "object",
                "required": ["threshold", "window_size"],
                "properties": {
                    "threshold": {"type": "number", "minimum": 0, "maximum": 1},
                    "window_size": {"type": "integer", "minimum": 1}
                }
            }
        )
    
    def initialize(self, config: Dict[str, Any], logger) -> bool:
        self.threshold = config.get('threshold', 0.8)
        self.window_size = config.get('window_size', 100)
        self.baseline_data = []
        return True
    
    def detect_threats(self, event: SecurityEvent) -> List[Dict[str, Any]]:
        # Simplified anomaly detection logic
        if event.event_type != SecurityEventType.NETWORK_ANOMALY:
            return []
        
        # Analyze network metrics
        network_data = event.data.get('network_metrics', {})
        anomaly_score = self._calculate_anomaly_score(network_data)
        
