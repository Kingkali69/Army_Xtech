class FeatureExtractor:
    """Extracts features from security events for ML processing"""
    
    def extract_features(self, event: SecurityEvent) -> List[float]:
        """Extract numerical features from security event"""
        features = []
        
        # Time-based features
        features.append(event.timestamp.hour / 24.0)  # Hour normalized
        features.append(event.timestamp.weekday() / 7.0)  # Weekday normalized
        
        # Event metadata features
        features.append(event.severity / 10.0)  # Severity normalized
        features.append(len(event.tags))  # Number of tags
        
        # Data-based features (simplified)
        data_size = len(str(event.data))
        features.append(min(data_size / 1000.0, 1.0))  # Data size normalized
        
        # Source-based features
        source_entropy = self._calculate_entropy(event.source)
        features.append(source_entropy)
        
        # Pad to fixed size (e.g., 20 features)
        while len(features) < 20:
            features.append(0.0)
        
        return features[:20]  # Ensure consistent size
    
    def _calculate_entropy(self, text: str) -> float:
        """Calculate Shannon entropy of text"""
        import math
        if not text:
            return 0.0
        
        char_counts = {}
        for char in text:
            char_counts[char] = char_counts.get(char, 0) + 1
        
        entropy = 0.0
        text_len = len(text)
        
        for count in char_counts.values():
            probability = count / text_len
            if probability > 0:
                entropy -= probability * math.log2(probability)
        
        return entropy / 8.0  # Normalize to 0-1 range

